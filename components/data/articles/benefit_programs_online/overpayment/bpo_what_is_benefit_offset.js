import Article from "../../../../common/article";

export default function bpo_what_is_benefit_offset() {
  return (
    <Article h4="What is a Benefit Offset?">
      <p>
        If you do not repay your overpayment promptly. the EDD will deduct
        the money owed from your future weekly Unemployment Insurance or
        State Disability Insurance benefits. This process is referred to
        as a benefit offset. The EDD may offset up to 100 percent of your
        benefits. Since the EDD cannot offset the penalty portion of an
        overpayment from your benefits, you must submit a payment to repay
        the penalty.
      </p>
    </Article>
  );
}

